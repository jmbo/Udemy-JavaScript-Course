const spendingLimits = Object.freeze({
  // makes object immutable -- only the first level though, it's not a "deep freeze"
  jonas: 1500,
  matilda: 100,
});
const getLimit = (limits, user) => limits?.[user] ?? 0;

// This is now a "pure function" -- does not mutate any outside objects / creates "side effects"
const addExpense = function (
  state,
  limits,
  value,
  description,
  user = 'jonas'
) {
  const cleanUser = user.toLowerCase();

  return value <= getLimit(limits, cleanUser)
    ? [...state, { value: -value, description, user: cleanUser }]
    : state;
};
// we can use composing to crate just one function instead and replace this chaining operations below to occur at once
const newBudget1 = addExpense(budget, spendingLimits, 10, 'Pizza ðŸ•');
const newBudget2 = addExpense(
  newBudget1,
  spendingLimits,
  100,
  'Going to movies ðŸ¿',
  'Matilda'
);
const newBudget3 = addExpense(newBudget2, spendingLimits, 200, 'Stuff', 'Jay');

// function is now nice and pure
const checkExpenses = (state, limits) =>
  state.map(entry =>
    entry.value < -getLimit(limits, entry.user)
      ? { ...entry, flag: 'limit' }
      : entry
  );

const finalBudget = checkExpenses(newBudget3, spendingLimits);
console.log(finalBudget);

// Impure function because it creates a side effect by having a console.log(), but that's ok!
